package dataService;

import java.util.ArrayList;

public class MultiOverallItem {
	private String id;
	private int maxPoint;
	private int number;
	private ArrayList<String> coworker;	//包括自己在内的协作者
	
	public MultiOverallItem(String id , int maxPoint,int number,ArrayList<String> coworker){
		this.id = id;
		this.maxPoint = maxPoint;
		this.number = number;
		this.coworker = coworker;
	}
	
	public String getId() {
		return id;
	}
	public int getMaxPoint() {
		return maxPoint;
	}
	public int getNumber() {
		return number;
	}
	public ArrayList<String> getCoworker(){
		return coworker;
	}
}
